package Day03;

public class PrimitiveDataTypes {

	
	/*
	 byte: we can assign numbers thats no decimal range -128 to 127
	 
	 short: We can only assign whole number. range must be with in -32768 to 32767
	 
	 int (used more often) : we can only assign whole number. Must be in -214.... to 214......
	 
	 long: we can only assign whole number.
	 
	 float: we can assign decimals.
	 
	 double: used more often : we can assign decimals.
	*/

	public static void main(String[] args) {
		
		 
		byte length = 3;
		
		System.out.println(length);
		
		byte width = 4;
		System.out.println(width);
		
		short num2 = 43;
		System.out.println(num2);
		
		int num4= -1000;
		
		float num7=num2;
		
		System.out.println(num7);
		
		double num9=num4;
		System.out.println(num9);
		
		
		
		
	}
	
	
}
