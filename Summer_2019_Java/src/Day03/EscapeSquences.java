package Day03;

public class EscapeSquences {

	/*
	 
	 
	 \n : Stars from a new line
	 \t: horizontal tab
	 \\: prints a back slash
	 \': prints single quote
	  \": prints double quote 
	 
	 */
	
	public static void main(String[] args) {
		
		
		// \n
		System.out.println("Hello Cybertek\n Batch 12 Students");
		
		System.out.print("Tomorrow is off \n");
		System.out.print("True\n");
		System.out.println("False\n\n\n");
		 
		// \t
		System.out.println("\tClass starts tomorrow");
		
		//  \\
		System.out.println("\\mansur");

		
		// \'
		System.out.println("\'Java");
		
		// \"
		System.out.println(" Its called \"Java\"");
		
		
	}
	
	
}
